package item;

public class ItemVo {
	int findStr;
	int pcode;
	String pname;
	int su;
	int price;
	
	public int getFindStr() {
		return findStr;
	}
	public void setFindStr(int findStr) {
		this.findStr = findStr;
	}
	public int getPcode() {
		return pcode;
	}
	public void setPcode(int pcode) {
		this.pcode = pcode;
	}
	public String getPname() {
		return pname;
	}
	public void setPname(String pname) {
		this.pname = pname;
	}
	public int getSu() {
		return su;
	}
	public void setSu(int su) {
		this.su = su;
	}
	public int getPrice() {
		return price;
	}
	public void setPrice(int price) {
		this.price = price;
	}
	
	
	
}
